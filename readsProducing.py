def Neville(x, quality_graph):
    if x == len(quality_graph):
        return quality_graph[-1]
    if x < 1:
        x = 1.0
    xt = [1.0, 2.0]
    w = [[0, 0], [0, 0]]
    m = [[0, 0], [0, 0]]
    index = int(x) - 1
    m[0][0] = quality_graph[index]
    m[1][0] = quality_graph[index + 1]
    x = 1 + x - int(x)
    for i in range(1, 2):
        for j in range(1, 2):
            w[i - j][i] = (x - xt[i - j]) / (xt[i] - xt[i - j])
            m[i][j] = m[i - 1][j - 1] + w[i - j][i] * (m[i][j - 1] - m[i - 1][j - 1])
    return m[1][1]


